        /// <summary>
        /// 获取客户端的 IP 信息
        /// </summary>
        /// <returns></returns>
        public static string GetClientIP()
        {
            try
            {

                string Ip = String.Empty;

                if (!string.IsNullOrEmpty(HttpContext.Current.Request.ServerVariables["HTTP_X_FORWARDED_FOR"]))
                {
                    if (HttpContext.Current.Request.ServerVariables["HTTP_X_FORWARDED_FOR"].IndexOf(",") == -1)
                    {
                        Ip = HttpContext.Current.Request.ServerVariables["HTTP_X_FORWARDED_FOR"].Trim();
                    }
                    else
                    {
                        Ip = HttpContext.Current.Request.ServerVariables["HTTP_X_FORWARDED_FOR"].ToString().Split(',')[0].Trim();
                    }

                    if (IsIp(Ip))
                    {
                        return Ip;
                    }
                }


                Ip = HttpContext.Current.Request.ServerVariables["REMOTE_ADDR"];

                if (IsIp(Ip))
                {
                    return Ip;
                }

                Ip = HttpContext.Current.Request.UserHostAddress;

                if (IsIp(Ip))
                {
                    return Ip;
                }

                return "0.0.0.0";
            }
            catch { }

            return "0.0.0.0";

        }


        public static bool IsIp(string Ip)
        {
            if (string.IsNullOrEmpty(Ip))
                return false;
            return Pool<IPRegex>.Instance.IsMatch(Ip);
        }


        /// <summary>
        /// 从网址中读取字符串流 post方式
        /// </summary>
        /// <param name="url"></param>
        /// <param name="param"></param>
        /// <returns></returns>
        public static string HttpPost(string url, string param)
        {
            HttpWebRequest request = (HttpWebRequest)HttpWebRequest.Create(url);
            request.Method = "POST";
            request.ContentType = "application/x-www-form-urlencoded";
            request.Accept = "*/*";
            request.Timeout = 10000;
            request.AllowAutoRedirect = false;

            StreamWriter requestStream = null;
            WebResponse response = null;
            string responseStr = null;

            try
            {
                requestStream = new StreamWriter(request.GetRequestStream());
                requestStream.Write(param);
                requestStream.Close();

                response = request.GetResponse();
                if (response != null)
                {
                    StreamReader reader = new StreamReader(response.GetResponseStream(), Encoding.UTF8);
                    responseStr = reader.ReadToEnd();
                    reader.Close();
                }
            }
            catch (Exception)
            {
                throw;
            }
            finally
            {
                request = null;
                requestStream = null;
                response = null;
            }

            return responseStr;
        }

